# Standardowe biblioteki
from pydantic import BaseModel

class Zastepstwo(BaseModel):
    zidentyfikowane: bool
    nauczyciel: str
    lekcja: int | None
    opis: str | None
    zastepca: str | None
    uwagi: str | None


class Zastepstwa(BaseModel):
    identyfikator: str | None
    dzien: str | None
    informacje: str
    zastepstwa: list[Zastepstwo]
